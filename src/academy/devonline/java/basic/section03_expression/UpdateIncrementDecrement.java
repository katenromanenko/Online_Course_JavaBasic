package academy.devonline.java.basic.section03_expression;

public class UpdateIncrementDecrement {
    public static void main(String[] args) {
        var a = 0;
        System.out.println(a);

        a = 5;
        System.out.println(a);

        a = a + 2;
        System.out.println(a);

        a += 1;
        System.out.println(a);

        a ++;
        System.out.println(a);

        a --;
        System.out.println(a);
    }
}
